#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (c < argc)
	{
		while (argv[c][i] != '\0')
			i++;
		write(1, argv[c], i);
		write(1, "\n", 1);
		i = 0;
		c++;
	}
	return (0);
}
